    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        // TODO add your handling code here:
       if (this.esValido()){
            if(this.getUsuario()){
                JPrincipal Pr = new JPrincipal(JBase_Datos,Connection_BD,  JUsuario.getText().trim());
                Pr.setVisible(true);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this,"Usuario/Clave errada al 3 intento se bloqueara");	
            }
       }else{
           JOptionPane.showMessageDialog(this,"Debe Ingresar Usuario y Clave");	
       }
    }//GEN-LAST:event_jBAceptarActionPerformed

    private void JClaveKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_JClaveKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()==10){
          if (this.esValido()){
             if(this.getUsuario()){
                JPrincipal Pr = new JPrincipal(JBase_Datos,Connection_BD,JUsuario.getText().trim());
                Pr.setVisible(true);
                this.dispose();
             }else{
                JOptionPane.showMessageDialog(this,"Usuario/Clave errada al 3 intento se bloqueara");	
             }
          }else{
             JOptionPane.showMessageDialog(this,"Debe Ingresar Usuario y Clave");	
          }
        }
    }//GEN-LAST:event_JClaveKeyPressed
    public boolean esValido(){
        boolean Control = true;
        if (JUsuario.getText().trim().equals("")){
            Control = false;
        }
        if ( JClave.getText().trim().equals("")){
            Control = false;
        }
        return Control;
    }
     public boolean getUsuario(){
        boolean Control = false;
        try {
            String Str_Sql =" select * from JUsuarios where codigo='"+JUsuario.getText().trim()+"' "
                            + " and clave= '"+JClave.getText().trim()+"' and estado= 'activo' ";
            ResultSet  Result = JBase_Datos.SQL_QRY(Connection_BD,Str_Sql);	
	    if(Result.next()){
                Control = true;
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(this,"JAccesoUsuarios() "+e.getMessage());	
        }
        return  Control;
    }
