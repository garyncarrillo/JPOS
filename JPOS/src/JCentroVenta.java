    public boolean Validacion_Crear(){
        boolean Sw=true;
        if(jTCodigo.getText().trim().equals("")){
            Sw=false;
        }
        if(this.jTDescripcion.getText().equals("")){
            Sw=false;
        }
        if(this.jComboBox1.getSelectedItem().toString().trim().equals("")){
            Sw=false;
        }
        return Sw; 
    }
