    public void ObtenerCajero_InitFacturacion(){
        try {
            if(!JTablaDetalleCajeros.getValueAt(JTablaDetalleCajeros.getSelectedRow(),0).toString().trim().equals("")){ 
                
                this.Caja.setCodigoCajeros(JTablaDetalleCajeros.getValueAt(JTablaDetalleCajeros.getSelectedRow(),0).toString().trim());
                this.Caja.setCentroCosto(this.CentroVenta.getCodigoCentroCosto());
                this.Caja.setNombre(JTablaDetalleCajeros.getValueAt(JTablaDetalleCajeros.getSelectedRow(),1).toString().trim());
                this.Caja.setEstado(JTablaDetalleCajeros.getValueAt(JTablaDetalleCajeros.getSelectedRow(),2).toString().trim());
                JFacturacion Ft = new JFacturacion(this.Pr,this.JBase_Datos, this.Cn, this.CentroVenta, this.Caja);
                Ft.setVisible(true);
                Pr.add(Ft);
                Pr.repaint();
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this,"Debe seleccionar un Cajero para Inciair");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"ObtenerCajero_InitFacturacion()"+e.getMessage());
        }
    }
    
    public void getAsignarCentroVenta(){
        try{
            String Str_Sql = "select  CodigoCentro, Descripcion from  JCentroCosto "
                        +"where Estado ='activo' ";
            jComboBox1.removeAllItems();
            jComboBox1.addItem("");
            ResultSet xResult =  JBase_Datos.SQL_QRY(this.Cn,Str_Sql);
            while(xResult.next()){
                jComboBox1.addItem(xResult.getString("Descripcion").trim());
            }
       }catch(Exception e ){
           JOptionPane.showMessageDialog(this,"JCentroCosto"+e.getMessage());
       }
       
    }
    public void ClearTable(){
        for (int i = 0; i <= Posicion; i++) {
                Detalle[i][0]="";
                Detalle[i][1]="";
                Detalle[i][2]="";
        }
    }
    
    public void getBuscarCajas(){
        try{
            String Str_Sql = "SELECT  t1.CodigoCentro, t1.Descripcion,t1.Estado as Est ,t2.CodigoCaja, t2.NombreCaja,  t2.Estado FROM  JCentroCosto as t1, JCajas as t2  "
                        +"WHERE t1.CodigoCentro= t2.CodigoCentro and t1.Estado ='activo' and t1.Descripcion= '"+jComboBox1.getSelectedItem().toString().trim()+"'";
            
            Cabecera[0]="Cod Caja";
            Cabecera[1]="Descripcion";
            Cabecera[2]="Estado";
            ResultSet Rs =  JBase_Datos.SQL_QRY(this.Cn,Str_Sql);
            this.ClearTable();
            Posicion = -1;
            while(Rs.next()){
                Posicion++;
                this.CentroVenta.setCentroCostos(Rs.getString("CodigoCentro"));
                this.CentroVenta.setNombre(Rs.getString("Descripcion"));
                this.CentroVenta.setEstado(Rs.getString("Est"));
                Detalle[Posicion][0]=Rs.getString("CodigoCaja");
                Detalle[Posicion][1]=Rs.getString("NombreCaja");
                Detalle[Posicion][2]=Rs.getString("Estado");
            }
          JTablaDetalleCajeros.setModel(new javax.swing.table.DefaultTableModel(Detalle, Cabecera));
        }catch(Exception e ){
           JOptionPane.showMessageDialog(this,"getBuscarCajas()"+e.getMessage());
        }
    }
    
