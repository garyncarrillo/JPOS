    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(this.Validacion_Crear()){
            if ( this.Centro_Costo.setCentroCosto(this.jTCodigo.getText().trim(), this.jTDescripcion.getText().trim(),jComboBox1.getSelectedItem().toString().trim()) ){
                JOptionPane.showMessageDialog(this,"Se guardo correctamente");
                jTbleCentroCosto.setValueAt(this.jTCodigo.getText().trim(),jTbleCentroCosto.getSelectedRow(), 0);
                jTbleCentroCosto.setValueAt(this.jTDescripcion.getText().trim(),jTbleCentroCosto.getSelectedRow(), 1);
                jTbleCentroCosto.setValueAt(jComboBox1.getSelectedItem().toString().trim(),jTbleCentroCosto.getSelectedRow(), 2);
            }
        }else{
            JOptionPane.showMessageDialog(this,"Debe diligenciar todo los Campos y en el formato correcto");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

