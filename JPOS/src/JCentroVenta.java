    private void jTbleCentroCostoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTbleCentroCostoMouseClicked
        // TODO add your handling code here:
        String BCodigo,BDescripcion, BEstado;
        try{
            BCodigo = jTbleCentroCosto.getValueAt(jTbleCentroCosto.getSelectedRow(),0).toString().trim();
            BDescripcion = jTbleCentroCosto.getValueAt(jTbleCentroCosto.getSelectedRow(),1).toString().trim();
            BEstado = jTbleCentroCosto.getValueAt(jTbleCentroCosto.getSelectedRow(),2).toString().trim();
            jTCodigo.setText(BCodigo.trim());
            jTDescripcion.setText(BDescripcion.trim());
            if(BEstado.equals("activo")){
                jComboBox1.setSelectedIndex(1);
            }else{
                jComboBox1.setSelectedIndex(2);
            }
        }catch(Exception e){
            jTCodigo.setText("");
            jTDescripcion.setText("");
            jComboBox1.setSelectedIndex(0);
        }
    }//GEN-LAST:event_jTbleCentroCostoMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(this.Validacion_Crear()){
           if(!this.Centro_Costo.Existe(this.jTCodigo.getText().trim())){
                if ( this.Centro_Costo.setCentroCosto(this.jTCodigo.getText().trim(), this.jTDescripcion.getText().trim(),jComboBox1.getSelectedItem().toString().trim()) ){
                   
                    this.Detalle = new Vector();
                    this.Detalle.add(this.jTCodigo.getText().trim());
                    this.Detalle.add(this.jTDescripcion.getText().trim());
                    this.Detalle.add(jComboBox1.getSelectedItem().toString().trim());
                    this.Detalle_Fila.add(this.Detalle);
                    jTbleCentroCosto.setModel(new javax.swing.table.DefaultTableModel(this.Detalle_Fila, Cabecera));
                }
           }else{
                JOptionPane.showMessageDialog(this,"Este centro ya existe por favor modificar el existente");   
           }
       }else{
            JOptionPane.showMessageDialog(this,"Debe diligenciar todo los Campos y en el formato correcto");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
